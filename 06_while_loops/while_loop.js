// While loop

// while loop is used to execute a block of code repeatedly until a condition is false.


// Example 1

// wap to print the numbers from 1 to 10 using while loop.

let i = 1;

while (i <= 10) {
    console.log(i);
    i++;
}

// Example 2

// wap to print the numbers from 10 to 1 using while loop.

let a = 10;

while (a >= 1) {
    console.log(a);
    a--;
}
