// Do while loop

// do while loop is used to execute a block of code repeatedly until a condition is false.

// do while loop is similar to while loop except that the condition is checked at the end of the loop.

// do while loop is used to execute a block of code at least once.

// Example 1

// wap to print the numbers from 1 to 10 using do while loop.

let i = 1;

do {
    console.log(i);
    i++;
}
while (i <= 10);

