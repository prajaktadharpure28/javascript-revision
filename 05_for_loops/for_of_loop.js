// For of loop

// for of loop is used to iterate over the elements of an array.

// for of loop is not used to iterate over objects.

// for of loop is used to iterate over arrays.

// Example 1

// wap to print the elements of an array using for of loop.

let arr = [1, 2, 3, 4, 5];

for (let element of arr) {
    console.log(element);
}
