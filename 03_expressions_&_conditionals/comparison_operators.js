// Comparison Operators

// Equal

let a = 10;
let b = 4;

console.log("a == b : ", a == b);
console.log("a == 10 : ", a == 10);

// Not Equal

console.log("a != b : ", a != b);
console.log("a != 10 : ", a != 10);

// Greater Than

console.log("a > b : ", a > b);
console.log("a > 10 : ", a > 10);

// Less Than

console.log("a < b : ", a < b);
console.log("a < 10 : ", a < 10);

// Greater Than or Equal To

console.log("a >= b : ", a >= b);
console.log("a >= 10 : ", a >= 10);

// Less Than or Equal To

console.log("a <= b : ", a <= b);
console.log("a <= 10 : ", a <= 10);

// Strict Equal (compares value and datatype)

console.log("a === b : ", a === b);
console.log("a === 10 : ", a === 10);

