// Assignment Operators

let a = 10;
let b = 4;

// a = a + b;
a += b;
console.log("a += b = ", a);

// a = a - b;
a -= b;
console.log("a -= b = ", a);

// a = a * b;
a *= b;
console.log("a *= b = ", a);

// a = a / b;
a /= b;
console.log("a /= b = ", a);

// a = a % b;
a %= b;
console.log("a %= b = ", a);

// a = a ** b;
a **= b;
console.log("a **= b = ", a);

