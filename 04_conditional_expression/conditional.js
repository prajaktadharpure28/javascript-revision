// Conditional Expression

// If Statement

let a = 10;
let b = 4;

if (a > b) {
    console.log("a is greater than b");
    }

// If Else Statement

if (a < b) {
    console.log("a is less than b");
    }
else {
    console.log("a is greater than b");
    }

// If Else If Statement

if (a < b) {
    console.log("a is less than b");
    }
else if (a > b) {
    console.log("a is greater than b");
    }
else {
    console.log("a is equal to b");
    }

// Ternary Operator

let c = 10;
let d = 4;

let result = (c > d) ? "c is greater than d" : "c is less than d";
console.log(result);

// Switch Statement

let e = 10;

switch (e) {
    case 1:
        console.log("e is equal to 1");
        break;
    case 2:
        console.log("e is equal to 2");
        break;
    case 3:
        console.log("e is equal to 3");
        break;
    case 4:
        console.log("e is equal to 4");
        break;
    case 5:
        console.log("e is equal to 5");
        break;
    default:
        console.log("e is greater than 5");
        break;
    }
