// Prompt and alert

// Prompt

// prompt is a function that displays a dialog box that prompts the user to input a value.

// The prompt function returns the value entered by the user, or null if the user clicks the Cancel button.

// The prompt function takes two parameters: a message to display and an optional default value.

// The message parameter is required, but the default value parameter is optional.

// The default value parameter is displayed in the text box as a hint to the user.

// The default value parameter is not required, but it is recommended.

// The default value parameter is a string.

// The prompt function is used to get input from the user.

// Alert

// alert is a function that displays a dialog box with a message and an OK button.

// The alert function takes one parameter: a message to display.

// The message parameter is required.

// The message parameter is a string.

// The alert function returns undefined.

// The alert function is used to display a message to the user.
